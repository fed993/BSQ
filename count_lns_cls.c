#include "my_file.h"

void	count_lns_cls(int *i)
{
	*i = 0;
	while (g_pre_grid[*i++])
		if (g_pre_grid[*i] == '\n')
			g_lns++;
	*i = 0;
	while (g_pre_grid[*i++] != '\n')
		g_cls++;
}
