#include "my_file.h"

void	find_max_num(int *max, int *i)
{
	*max = 0;
	*i = 0;

	while (*i < (g_cls + 1) * g_lns)
	{
		if (g_grid[*i] > *max && g_grid[*i] != -2)
			*max = g_grid[*i];
		*i++;
	}
}
